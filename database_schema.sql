  base_route text, hierarchy_level integer, permissions text[]
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  RETURN QUERY
  SELECT
    e.id,
    e.username::TEXT,
    e.full_name::TEXT,
    e.role::TEXT,
    e.is_active,
    e.profile_id,
    p.name::TEXT AS profile_name,
    p.display_name::TEXT AS profile_display_name,
    p.base_route::TEXT,
    p.hierarchy_level,
    COALESCE(
      ARRAY(
        SELECT pp.permission_id::TEXT
        FROM profile_permissions pp
        WHERE pp.profile_id = e.profile_id
        ORDER BY pp.permission_id
      ),
      ARRAY[]::TEXT[]
    ) AS permissions
  FROM employees e
  LEFT JOIN employee_profiles p ON p.id = e.profile_id
  WHERE e.username = p_username
    AND e.password_hash = crypt(p_password, e.password_hash)
    AND e.is_active = TRUE;
END;
$$;

