END;
$$;

-- send_otp()
CREATE OR REPLACE FUNCTION public.send_otp(p_email text)
RETURNS json
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_code text;
  v_expires_at timestamptz;
BEGIN
  v_code := lpad(floor(random() * 10000)::text, 4, '0');
  v_expires_at := now() + interval '10 minutes';
  
  UPDATE public.otp_codes 
  SET expires_at = now() 
  WHERE email = lower(p_email) 
    AND verified = false 
    AND expires_at > now();
  
  INSERT INTO public.otp_codes (email, code, expires_at)
  VALUES (lower(p_email), v_code, v_expires_at);
  
  RETURN json_build_object(
    'success', true,
    'message', 'Code envoyé à ' || p_email,
    'expires_in_seconds', 600
  );
END;
$$;

-- verify_otp()
CREATE OR REPLACE FUNCTION public.verify_otp(p_email text, p_code text)
RETURNS json
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_otp_record record;
  v_user_id uuid;
  v_is_new_user boolean := false;
BEGIN
  SELECT * INTO v_otp_record
  FROM public.otp_codes
  WHERE email = lower(p_email)
    AND verified = false
    AND expires_at > now()
  ORDER BY created_at DESC
  LIMIT 1;
  
  IF v_otp_record IS NULL THEN
    RETURN json_build_object(
      'success', false,
      'error', 'CODE_EXPIRED',
      'message', 'Code expiré ou invalide. Veuillez demander un nouveau code.'
    );
  END IF;
  
  IF v_otp_record.attempts >= 5 THEN
    UPDATE public.otp_codes SET expires_at = now() WHERE id = v_otp_record.id;
    RETURN json_build_object(
      'success', false,
      'error', 'TOO_MANY_ATTEMPTS',
      'message', 'Trop de tentatives. Veuillez demander un nouveau code.'
    );
  END IF;
  
  UPDATE public.otp_codes 
  SET attempts = attempts + 1 
  WHERE id = v_otp_record.id;
  
  IF v_otp_record.code != p_code THEN
    RETURN json_build_object(
      'success', false,
      'error', 'INVALID_CODE',
      'message', 'Code incorrect. ' || (4 - v_otp_record.attempts) || ' tentatives restantes.',
      'attempts_remaining', 4 - v_otp_record.attempts
    );
  END IF;
  
  UPDATE public.otp_codes 
  SET verified = true 
  WHERE id = v_otp_record.id;
  
  SELECT id INTO v_user_id
  FROM auth.users
  WHERE email = lower(p_email)
  LIMIT 1;
  
  IF v_user_id IS NULL THEN
    v_is_new_user := true;
  END IF;
  
  RETURN json_build_object(
    'success', true,
    'message', 'Code vérifié avec succès',
    'is_new_user', v_is_new_user,
    'user_id', v_user_id
  );
END;
$$;

-- get_available_slots()
CREATE OR REPLACE FUNCTION public.get_available_slots(p_date date)
RETURNS TABLE(
  terrain_id bigint, 
  terrain_code text, 
  time_slot_id bigint, 
  start_time time, 
  end_time time, 
  price integer, 
  is_reserved boolean
)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  RETURN QUERY
  SELECT 
    t.id as terrain_id,
    t.code as terrain_code,
    ts.id as time_slot_id,
    ts.start_time,
    ts.end_time,
    ts.price,
    EXISTS (
      SELECT 1 FROM public.reservations r
      WHERE r.terrain_id = t.id 
        AND r.time_slot_id = ts.id
        AND r.reservation_date = p_date
        AND r.status IN ('CONFIRMED', 'PENDING', 'PAID')
    ) as is_reserved
  FROM public.terrains t
  CROSS JOIN public.time_slots ts
  WHERE t.is_active = true AND ts.is_active = true
  ORDER BY ts.start_time, t.code;
END;
$$;

-- calculate_reservation_points()
CREATE OR REPLACE FUNCTION public.calculate_reservation_points(p_time_slot_id bigint)
RETURNS integer
LANGUAGE plpgsql
AS $$
DECLARE
  v_start_time TIME;
  v_end_time TIME;
  v_duration_minutes INTEGER;
BEGIN
  SELECT start_time, end_time INTO v_start_time, v_end_time
  FROM time_slots WHERE id = p_time_slot_id;
  
  IF v_start_time IS NULL THEN
    RETURN 0;
  END IF;
  
  v_duration_minutes := EXTRACT(EPOCH FROM (v_end_time - v_start_time)) / 60;
  
  IF v_duration_minutes >= 90 THEN
    RETURN 2;
  ELSE
    RETURN 1;
  END IF;
END;
$$;

-- add_reservation_points()
CREATE OR REPLACE FUNCTION public.add_reservation_points(p_reservation_id bigint)
RETURNS json
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_user_id UUID;
  v_time_slot_id BIGINT;
  v_points INTEGER;
  v_new_total INTEGER;
BEGIN
  SELECT user_id, time_slot_id INTO v_user_id, v_time_slot_id
  FROM reservations WHERE id = p_reservation_id;
  
  IF v_user_id IS NULL THEN
    RETURN json_build_object('success', false, 'error', 'Reservation not found');
  END IF;
  
  v_points := calculate_reservation_points(v_time_slot_id);
  
  UPDATE profiles 
  SET points = COALESCE(points, 0) + v_points
  WHERE id = v_user_id
  RETURNING points INTO v_new_total;
  
  RETURN json_build_object(
    'success', true,
    'points_earned', v_points,
    'total_points', v_new_total
  );
END;
$$;

