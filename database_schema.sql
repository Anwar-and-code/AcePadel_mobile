RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  IF NEW.password_hash IS NOT NULL 
     AND NEW.password_hash != '' 
     AND NEW.password_hash NOT LIKE '$2a$%' 
     AND NEW.password_hash NOT LIKE '$2b$%' THEN
    NEW.password_hash := crypt(NEW.password_hash, gen_salt('bf'));
