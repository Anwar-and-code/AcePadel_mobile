-- verify_employee_password()
CREATE OR REPLACE FUNCTION public.verify_employee_password(p_username varchar, p_password varchar)
RETURNS TABLE(id uuid, username varchar, full_name varchar, role varchar, is_active boolean)
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  RETURN QUERY
  SELECT 
    e.id,
    e.username,
    e.full_name,
    e.role,
    e.is_active
  FROM public.employees e
  WHERE e.username = p_username
    AND e.password_hash = crypt(p_password, e.password_hash)
    AND e.is_active = true;
  
  UPDATE public.employees 
  SET last_login_at = NOW() 
  WHERE employees.username = p_username 
    AND employees.password_hash = crypt(p_password, employees.password_hash);
END;
$$;

