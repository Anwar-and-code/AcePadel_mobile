CREATE OR REPLACE VIEW public.slot_availability AS
SELECT 
  t.id AS terrain_id,
  t.code AS terrain_code,
  ts.id AS time_slot_id,
  ts.start_time,
  ts.end_time,
  ts.price,
  r.reservation_date,
  CASE
    WHEN (r.id IS NOT NULL AND r.status = ANY (ARRAY['CONFIRMED'::reservation_status, 'PENDING'::reservation_status]))
    THEN true
    ELSE false
  END AS is_reserved,
  r.id AS reservation_id,
  r.user_id AS reserved_by
FROM terrains t
CROSS JOIN time_slots ts
LEFT JOIN reservations r 
  ON r.terrain_id = t.id 
  AND r.time_slot_id = ts.id 
  AND r.status = ANY (ARRAY['CONFIRMED'::reservation_status, 'PENDING'::reservation_status])
WHERE t.is_active = true AND ts.is_active = true;

